func readKeyring(r io.Reader) openpgp.EntityList {
	keyring, err := openpgp.ReadArmoredKeyRing(r)
	if err != nil {
		panic(err)
	}
	return keyring
}

func readKeyringString(s string) openpgp.EntityList {
	return readKeyring(strings.NewReader(s))
}

